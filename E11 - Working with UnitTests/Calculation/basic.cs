using System;

namespace Calculation
{
    public class basic
    {
        public double Ddivide(int a, int b)
        {
            return (double)a /(double) b;
        }
      
        public double Mmultiply(int a, int b)
        {
            return (double)a * (double)b;
        }
        public double Aadd(int a, int b)
        {
            return (double)a + (double)b;
        }
        public double Ssubtract(int a, int b)
        {
            return (double)a - (double)b;
        }
    }
}
