using System;
using System.Collections.Generic;
using System.Text;

namespace Ex10.Calculation
{
    public class Calculator
    {
        public static double Add(double firstArgs, double secondArgs) {
            return firstArgs + secondArgs;
        }

        public static double Substract(double firstArgs, double secondArgs)
        {
            return firstArgs - secondArgs;
        }
    }
}
