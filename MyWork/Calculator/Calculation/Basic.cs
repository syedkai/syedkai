using System;

namespace Calculation
{
    public static class basic
    {
        public static double Ddivide(int a,int b) {
            return (double)a / (double)b;
        }
        public static double Mmultiply(int a,int b) {
            return (double)a * (double)b;
        }
        public static double Aadd(int a, int b) {
            return (double)a + (double)b;
        }
        public static double Ssubtract(int a, int b) {
            return (double)a - (double)b;
        }
    }
}
