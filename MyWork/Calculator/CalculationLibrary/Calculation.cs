using System;

namespace CalculationLibrary
{
        public static class Calculation
        {
            public static double Mmultiply(int a, int b)
            {
                return (double)a * (double)b;
            }
            public static double Ddivide(int a,int b)
            {
                return (double)a / (double)b;
            }
            public static double Pplus(int a, int b)
            {
                return (double)a + (double)b;
            }
            public static double Mminus(int a, int b)
            {
                return (double)a - (double)b;
            }
        }
    }
